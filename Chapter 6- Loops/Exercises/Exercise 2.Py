age = int(input(f"How old are you?:"))

if age < 3: #if the age is less than 3 
    print ("Your ticket cost is free!")
elif age >= 3 and age <= 12: #if the age is between 3 and 12  
    print ("Your ticket cost is $10")
else: #if the age is more than 12
    print ("Your ticket cost is $15")
