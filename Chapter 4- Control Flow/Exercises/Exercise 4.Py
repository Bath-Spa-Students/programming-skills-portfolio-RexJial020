
Age = 19

if Age < 2:
    print ("The person is a baby.")
elif Age < 4:
    print ("The person is a toddler.")
elif Age < 13:
    print ("The person is a kid.")
elif Age < 20:
    print ("The person is a teenager.")
elif Age < 65:
    print ("The person is an adult.")
else:
    print ("The person is an elder.")
